    {
        return [
            'uid'=> User::all()->random()->id,
            'title' => $this->faker->unique()->sentence(),
            'complated' => $this->faker->boolean(),
            'created_at' => date('Y-m-d H:i:s'),
            'updated_at' => date('Y-m-d H:i:s'),
        ];
    }
}
